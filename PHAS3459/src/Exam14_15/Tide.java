package Exam14_15;

public class Tide {
	private String name;
	private int year;
	private int month;
	private int day;
	private int hour;
	private int min;
	private double level;
	private double predLevel;

	public Tide(String name, int year, int month, int day, int hour, int min, double level, double predLevel ) {
		this.name = name;
		this.year = year;
		this.month = month;
		this.day = day;
		this.hour = hour;
		this.min = min;
		this.level = level;
		this.predLevel = predLevel;
	}
	
	
}
